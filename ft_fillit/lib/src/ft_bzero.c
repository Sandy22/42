#include <libft.h>

void	ft_bzero(void *s, size_t n)
{
	char	*res;
	size_t	i;

	res = s;
	i = 0;
	if (n <= 0)
		return ;
	while (n > i)
	{
		*(res + i) = 0;
		i++;
	}
}
